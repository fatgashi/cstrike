<template>
  <div class="app">
    <router-view></router-view>
    <LoginModal ref="loginModal" />
  </div>
</template>

<script>
import LoginModal from './components/LoginModal.vue';
import { eventBus } from './router';
export default {
  name: 'App',
  components: {
    LoginModal,
  },
  mounted() {
    // ‚úÖ Listen for the event
    eventBus.on("showLoginModal", () => {
      console.log("üî• showLoginModal event received in App.vue");
      if (this.$refs.loginModal) {
        this.$refs.loginModal.modal.show();
      } else {
        console.error("‚ùå LoginModal component reference not found.");
      }
    });
  }
}
</script>

